# ê³µê°„ ë³µìž¡ë„ëŠ” O(1)
#   ê³µê°„ ë³µìž¡ë„ ê³„ì‚°ì€ ì‹¤ì œ ì•Œê³ ë¦¬ì¦˜ ì‹¤í–‰ì‹œ ì‚¬ìš©ë˜ëŠ” ì €ìž¥ê³µê°„ì„ ê³„ì‚°í•˜ë©´ ë¨

def factorial(n):
    fac = 1
    for index in range(2, n + 1):
        fac = fac * index
    return fac

print(factorial(3))

